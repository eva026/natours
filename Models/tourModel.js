tourSchema.pre(/^find/, function (next) {
  this.find({ secretTour: { $ne: true } });
  this.start = Date.now();
  next();
});

tourSchema.post(/^find/, function (docs, next) {
  console.log(`It takes ${Date.now() - this.start} milliseconds.`);
  next();
});

tourSchema.pre(/^find/, function (next) {
  this.populate({
    path: 'guides',
    select: '-__v -passwordChangedAt',
  });
  next();
});
// AGGREGATION MIDDLEWARE
tourSchema.pre('aggregate', function (next) {
  // Check if the first in pipeline is $geoNear(Object.keys returns an array)
  const first = Object.keys(this.pipeline()[0])[0];
  if (first === '$geoNear') {
    //don't match secret tours, but splice "$match" stage after $geoNear in pipeline
    this.pipeline().splice(1, 0, { $match: { secretTour: { $ne: true } } });
    return next();
  }
  // Add $match to the beginning of the pipeline
  this.pipeline().unshift({ $match: { secretTour: { $ne: true } } });
  next();
});
const Tour = mongoose.model('Tour', tourSchema);

module.exports = Tour;
